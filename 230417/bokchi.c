int DeathCheck(int cnt, int type)  //죽을지 말지 결정 cnt에 death_cnt값을 받아 죽은적이 있었는지 확인 , type은 1먹이 나머지 모험 확률
{
  int result=0; //1이면 죽음 0이면 생존
  double check; //난수저장을 위한 변수
  check=(rand()%1000+1)*0.1; //비교를 위해 난수 저장

  if(type==1) //먹이
  {
    if(cnt==0) //죽은적이 없다면 생존확률 92%
    {
     if(check<9)  //8%확률로 죽음
     result=1;
    }
    else  //죽은적이 있다면 생존확률 99.9%
    {
      if(check<0.2) //0.1%의 확률로 죽음
      result=1;
    }
  }
  else  //모험
  {
    if(cnt==0)
    {
      if(check<51)  //50%의 확률로 죽음
      result=1;
    }
    else if(cnt==1)
    {
      if(check<26)  //25%의 확률로 죽음
      result=1;
    }
    else if(cnt==2)
    {
      if(check<6) //5%의 확률로 죽음
      result=1;
    }
    else
    {
      if(check<2) //1%의 확률로 죽음
      result=1;
    }
  }
  return result;
}

